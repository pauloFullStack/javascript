<script>
    //Como o nome deste método já dá a entender, ele inverte os valores do array, de maneira que o primeiro se tornará o último, e o último se tornará o primeiro. Exemplo:

    const meuArray = [1, 2, 3, 4, 5];

    const novoArray = meuArray.reverse();

    console.log(novoArray);
//[5, 4, 3, 2, 1]

//Este método também retorna um novo array, que no caso será o original invertido, então você pode passar o resultado desse método para uma variável como em alguns dos outros métodos
</script>